#[derive(Clone, Copy)]
pub struct Rgb {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Rgb {
    pub const BLACK: Self = Rgb {
        r: 0x00,
        g: 0x00,
        b: 0x00,
    };
    pub const CYAN: Self = Rgb {
        r: 0x00,
        g: 0xE5,
        b: 0xFF,
    };
    pub const WHITE: Self = Rgb {
        r: 0xFF,
        g: 0xFF,
        b: 0xFF,
    };
    pub const DARK_BG: Self = Rgb {
        r: 0x0A,
        g: 0x0A,
        b: 0x1A,
    };
}

// ---------------------------------------------------------------------------
// Framebuffer renderer
// ---------------------------------------------------------------------------

pub struct FramebufferRenderer {
    buf: &'static mut [u8],
    info: FrameBufferInfo,
}

impl FramebufferRenderer {
    pub fn new(buf: &'static mut [u8], info: FrameBufferInfo) -> Self {
        Self { buf, info }
    }

    fn bytes_per_pixel(&self) -> usize {
        self.info.bytes_per_pixel
    }

    fn stride(&self) -> usize {
        self.info.stride
    }

    fn set_pixel(&mut self, x: usize, y: usize, color: Rgb) {
        if x >= self.info.width || y >= self.info.height {
            return;
        }
        let bpp = self.bytes_per_pixel();
        let offset = (y * self.stride() + x) * bpp;
        if offset + bpp > self.buf.len() {
            return;
        }
        match self.info.pixel_format {
            PixelFormat::Rgb => {
                self.buf[offset] = color.r;
                self.buf[offset + 1] = color.g;
                self.buf[offset + 2] = color.b;
                if bpp >= 4 {
                    self.buf[offset + 3] = 0xFF;
                }
            }
            PixelFormat::Bgr => {
                self.buf[offset] = color.b;
                self.buf[offset + 1] = color.g;
                self.buf[offset + 2] = color.r;
                if bpp >= 4 {
                    self.buf[offset + 3] = 0xFF;
                }
            }
            PixelFormat::U8 => {
                let luma = (color.r / 3)
                    .wrapping_add(color.g / 3)
                    .wrapping_add(color.b / 3);
                self.buf[offset] = luma;
            }
            _ => {}
        }
    }

    /// Fill the entire framebuffer with `color`.
    pub fn clear(&mut self, color: Rgb) {
        let w = self.info.width;
        let h = self.info.height;
        for y in 0..h {
            for x in 0..w {
                self.set_pixel(x, y, color);
            }
        }
    }

    /// Draw a single 8×16 character at pixel position (`px`, `py`).
    fn draw_char(&mut self, ch: char, px: usize, py: usize, fg: Rgb, scale: usize) {
        let idx = ch as usize;
        if !(0x20..=0x7e).contains(&idx) {
            return;
        }
        let glyph = &FONT[idx - 0x20];
        for (row, &bits) in glyph.iter().enumerate() {
            for col in 0..8usize {
                let set = (bits >> (7 - col)) & 1 != 0;
                if set {
                    for sy in 0..scale {
                        for sx in 0..scale {
                            self.set_pixel(px + col * scale + sx, py + row * scale + sy, fg);
                        }
                    }
                }
            }
        }
    }

    /// Draw a string starting at (`px`, `py`) with the given scale factor.
    pub fn draw_str(&mut self, s: &str, px: usize, py: usize, fg: Rgb, scale: usize) {
        let char_w = 8 * scale;
        let mut x = px;
        for ch in s.chars() {
            self.draw_char(ch, x, py, fg, scale);
            x += char_w;
        }
    }

    /// Render a horizontal line.
    pub fn hline(&mut self, x: usize, y: usize, len: usize, color: Rgb) {
        for i in 0..len {
            self.set_pixel(x + i, y, color);
        }
    }

    /// Render the argonOS boot splash.
    pub fn draw_splash(&mut self) {
        let w = self.info.width;
        let h = self.info.height;

        // Background fill.
        self.clear(Rgb::DARK_BG);

        // --- Large "argonOS" title (scale 4, centred vertically ~40 % down) ---
        let title = "argonOS";
        let scale = 4usize;
        let char_w = 8 * scale;
        let char_h = 16 * scale;
        let total_w = title.len() * char_w;
        let tx = w.saturating_sub(total_w) / 2;
        let ty = h / 2 - char_h;
        self.draw_str(title, tx, ty, Rgb::CYAN, scale);

        // Decorative underline.
        let line_y = ty + char_h + scale * 2;
        self.hline(tx, line_y, total_w, Rgb::CYAN);

        // --- Sub-label at scale 2 ---
        let sub = "Rust  |  x86_64  |  Secure";
        let sub_w = sub.len() * 8 * 2;
        let sx = w.saturating_sub(sub_w) / 2;
        let sy = line_y + scale * 4;
        self.draw_str(sub, sx, sy, Rgb::WHITE, 2);

        // Bottom status bar.
        let bar_y = h - 32;
        self.hline(0, bar_y, w, Rgb::CYAN);
        let status = "v0.1.0   SMEP/SMAP ON   W^X ON   IDT LOADED";
        self.draw_str(status, 8, bar_y + 8, Rgb::WHITE, 1);
    }
}
